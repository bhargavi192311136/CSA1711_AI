# List Operations
nested_list = [[1, 2], [3, 4], [5, 6]]

# Length
print("Length:", len(nested_list))

# Concatenation
list1 = [1, 2, 3]
list2 = [4, 5]
print("Concatenation:", list1 + list2)

# Membership
print("Is 3 in list1?", 3 in list1)

# Iteration
print("Iterating list1:")
for item in list1:
    print(item, end=" ")

# Indexing
print("\nIndexing list1[1]:", list1[1])

# Slicing
print("Slicing list1[0:2]:", list1[0:2])
